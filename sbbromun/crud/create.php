<?php 

	session_start();	
	if(empty($_SESSION['name'])) {header("Location: login.php");}


	#Consider 3 scenarios
	# 1. The user clicked create on list screen (Index.php)
	#	If that happens then create.php displays entry screen.
	# 2. User clicked create button (Submit button) on entry screen with empty field.
	#	If that happens then error message appear next to empty field(s)
	# 3. User clicks create button (submit button) on entry screen with all valid data
	#	If that happens then the PHP code inserts record into database and redirects to list screen.

	# include connection data and functions
	require 'database.php';
	#IF there was data passed, then insert the record.
	#otherwise do nothing(that is, just display screen.)
	if ( !empty($_POST)) {
		// keep track validation errors
		$nameError = null;
		$emailError = null;
		$mobileError = null;
		
		// keep track post values
		$name = $_POST['name'];
		$email = $_POST['email'];
		$mobile = $_POST['mobile'];
		
		// validate input
		$valid = true;
		if (empty($name)) {
			$nameError = 'Please enter Name';
			$valid = false;
		}
		
		if (empty($email)) {
			$emailError = 'Please enter Email Address';
			$valid = false;
		} else if ( !filter_var($email,FILTER_VALIDATE_EMAIL) ) {
			$emailError = 'Please enter a valid Email Address';
			$valid = false;
		}
		
		if (empty($mobile)) {
			$mobileError = 'Please enter Mobile Number';
			$valid = false;
		}
		
		// insert data
		if ($valid) {
			$pdo = Database::connect();
			$pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
			$sql = "INSERT INTO customers (name,email,mobile) values(?, ?, ?)";
			$q = $pdo->prepare($sql);
			$q->execute(array($name,$email,$mobile));
			Database::disconnect();
			header("Location: index.php");
		}
	}#END if(!empty($_POST))
?>


